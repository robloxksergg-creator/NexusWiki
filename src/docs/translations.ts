// Fill missing bodies with the Russian stub.
export function localizeSection(section: DocSection): DocSection {
  const l = sectionsRu[section.slug];
  return {
    ...section,
    title: l?.title ?? section.title,
    pages: section.pages.map((p) => {
      const pl = pagesRu[section.slug]?.[p.slug];
      return {
        ...p,
        title: pl?.title ?? p.title,
        description: pl?.description ?? p.description,
        body: pl?.body ?? stubRu(pl?.title ?? p.title, pl?.description ?? p.description),
      };
    }),
  };
}
